# 1. Số (Numbers)
a = 10          # int
b = 3.14        # float
c = 2 + 3j      # complex

print("a:", a, type(a))
print("b:", b, type(b))
print("c:", c, type(c))

# 2. Chuỗi (String)
s = "Hello Python"
print("s:", s, type(s))

# 3. Kiểu logic (Boolean)
x = True
y = False
print("x:", x, type(x))
print("y:", y, type(y))

# 4. Danh sách (List)
my_list = [1, 2, 3, "apple"]
print("my_list:", my_list, type(my_list))

# 5. Bộ (Tuple)
my_tuple = (10, 20, 30)
print("my_tuple:", my_tuple, type(my_tuple))

# 6. Tập hợp (Set)
my_set = {1, 2, 3, 2}
print("my_set:", my_set, type(my_set))   # sẽ tự loại bỏ phần tử trùng

# 7. Từ điển (Dictionary)
my_dict = {"name": "Alice", "age": 20}
print("my_dict:", my_dict, type(my_dict))

# 8. NoneType
z = None
print("z:", z, type(z))